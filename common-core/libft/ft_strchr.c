#include "libft.h"

char	*ft_strchr(const char *str, int c)
{
	char	*s;

	s = (char *)str;
	while (*s && *s != (char)c)
		s++;
	if (*s == '\0' && c == '\0')
		return (s);
	if (!*s && *s != (char)c)
		return (NULL);
	return (s);
}
