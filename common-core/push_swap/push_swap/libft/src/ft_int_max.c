#include "../libft.h"

int	ft_int_max(char const *s)
{
	long long	num;

	if (s[0] == '-')
		return (0);
	if (ft_strlen(s) > 10)
		return (1);
	if (ft_strlen(s) == 10)
	{
		num = ft_atoll(s);
		if (num > 2147483647)
			return (1);
	}
	return (0);
}
