#include "../libft.h"

long long	ft_atoll(const char *str)
{
	int			i;
	int			neg;
	long long	nb;

	i = 0;
	nb = 0;
	neg = 1;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			neg *= -1;
		i++;
	}
	while (str[i] >= 48 && str[i] <= 57)
	{
		nb = nb * 10 + str[i] - 48;
		i++;
		if (nb * neg > 9223372036854775807LL)
			return (9223372036854775807LL);
		else if (nb * neg < -9223372036854775807LL - 1)
			return (-9223372036854775807LL - 1);
	}
	return (nb * neg);
}
