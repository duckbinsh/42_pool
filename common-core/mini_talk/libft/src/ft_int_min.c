#include "../libft.h"

int	ft_int_min(char const *s)
{
	if (ft_strlen(s) >= 11 && (s[1] > '2' || s[2] > '1' || s[3] > '4'
			|| s[4] > '7' || s[5] > '4' || s[6] > '8' || s[7] > '3'
			|| s[8] > '6' || s[9] > '4' || s[10] > '8'))
		return (1);
	return (0);
}
